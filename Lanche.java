abstract class Lanche {

	Carne Ingrediente1;
	Queijo Ingrediente2;
	Salada Ingrediente3;
	Molho Ingrediente4;
	
	Lanche(Carne Ingrediente1, Queijo Ingrediente2, Salada Ingrediente3, Molho Ingrediente4) {
		this.Ingrediente1 = Ingrediente1;		
		this.Ingrediente2 = Ingrediente2;		
		this.Ingrediente3 = Ingrediente3;		
		this.Ingrediente4 = Ingrediente4;
	}	
	
}
